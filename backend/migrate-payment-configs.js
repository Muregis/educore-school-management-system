import { supabase } from './src/config/supabaseClient.js';

async function createPaymentConfigsTable() {
  console.log('Creating payment_configs table...');
  
  // Create table first
  const createTableSQL = `
    CREATE TABLE IF NOT EXISTS public.payment_configs (
      config_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      school_id BIGINT NOT NULL REFERENCES public.schools(school_id) ON DELETE CASCADE,
      
      -- M-Pesa Configuration
      mpesa_till_number VARCHAR(20) NULL,
      mpesa_paybill VARCHAR(20) NULL,
      mpesa_shortcode VARCHAR(20) NULL,
      mpesa_consumer_key TEXT NULL,
      mpesa_consumer_secret TEXT NULL,
      mpesa_passkey TEXT NULL,
      mpesa_environment VARCHAR(20) NOT NULL DEFAULT 'sandbox',
      mpesa_base_url VARCHAR(255) NULL,
      mpesa_callback_base_url VARCHAR(255) NULL,
      
      -- Paystack Configuration
      paystack_public_key TEXT NULL,
      paystack_secret_key TEXT NULL,
      paystack_environment VARCHAR(20) NOT NULL DEFAULT 'test',
      paystack_callback_url VARCHAR(255) NULL,
      
      -- Bank Configuration
      bank_account_number VARCHAR(50) NULL,
      bank_name VARCHAR(100) NULL,
      bank_branch VARCHAR(100) NULL,
      account_name VARCHAR(200) NULL,
      
      -- SMS Configuration
      sms_enabled BOOLEAN NOT NULL DEFAULT false,
      sms_sender_id VARCHAR(20) NULL,
      
      -- Metadata
      is_active BOOLEAN NOT NULL DEFAULT true,
      is_deleted BOOLEAN NOT NULL DEFAULT false,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      
      CONSTRAINT unique_active_school_config UNIQUE (school_id) WHERE (is_active = true AND is_deleted = false)
    );
  `;
  
  try {
    const { error: tableError } = await supabase.rpc('exec_sql', { 
      sql_query: createTableSQL 
    });
    
    if (tableError && !tableError.message.includes('already exists')) {
      console.log('Table creation error:', tableError.message);
    }
  } catch (err) {
    console.log('Table creation failed:', err.message);
  }
  
  // Create indexes
  const indexesSQL = `
    CREATE INDEX IF NOT EXISTS idx_payment_configs_school_id ON public.payment_configs(school_id);
    CREATE INDEX IF NOT EXISTS idx_payment_configs_active ON public.payment_configs(is_active, is_deleted);
  `;
  
  try {
    const { error: indexError } = await supabase.rpc('exec_sql', { 
      sql_query: indexesSQL 
    });
    
    if (indexError) {
      console.log('Index creation error:', indexError.message);
    }
  } catch (err) {
    console.log('Index creation failed:', err.message);
  }
  
  // Enable RLS
  const rlsSQL = `
    ALTER TABLE public.payment_configs ENABLE ROW LEVEL SECURITY;
    
    CREATE POLICY IF NOT EXISTS "Schools can view own payment configs" ON public.payment_configs
      FOR SELECT USING (school_id = auth.jwt() ->> 'school_id'::text);
      
    CREATE POLICY IF NOT EXISTS "Schools can insert own payment configs" ON public.payment_configs  
      FOR INSERT WITH CHECK (school_id = auth.jwt() ->> 'school_id'::text);
      
    CREATE POLICY IF NOT EXISTS "Schools can update own payment configs" ON public.payment_configs
      FOR UPDATE USING (school_id = auth.jwt() ->> 'school_id'::text);
  `;
  
  try {
    const { error: rlsError } = await supabase.rpc('exec_sql', { 
      sql_query: rlsSQL 
    });
    
    if (rlsError) {
      console.log('RLS creation error:', rlsError.message);
    }
  } catch (err) {
    console.log('RLS creation failed:', err.message);
  }
  
  // Test table creation
  try {
    const { data, error } = await supabase
      .from('payment_configs')
      .select('*')
      .limit(1);
    
    if (error) {
      console.log('Table verification failed:', error.message);
    } else {
      console.log('✅ payment_configs table created successfully!');
    }
  } catch (err) {
    console.log('Verification error:', err.message);
  }
}

createPaymentConfigsTable().catch(console.error);
