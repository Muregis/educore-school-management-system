-- EduCore Postgres/Supabase schema (grades dependency)
-- Creates ONLY the `results` table (depends on `schools`, `students`, `classes`, `teachers`).
--
-- Notes vs MySQL:
-- - DECIMAL(6,2) stays numeric(6,2)

CREATE TABLE IF NOT EXISTS public.results (
  result_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  school_id       BIGINT NOT NULL REFERENCES public.schools(school_id),
  student_id      BIGINT NOT NULL REFERENCES public.students(student_id),
  subject         VARCHAR(120) NOT NULL,
  term            VARCHAR(40) NOT NULL DEFAULT 'Term 2',
  marks           NUMERIC(6,2) NOT NULL,
  total_marks     NUMERIC(6,2) NOT NULL DEFAULT 100,
  grade           VARCHAR(8) NULL,
  class_id        BIGINT NULL REFERENCES public.classes(class_id),
  teacher_id      BIGINT NULL REFERENCES public.teachers(teacher_id),
  teacher_comment VARCHAR(255) NULL,
  is_deleted      BOOLEAN NOT NULL DEFAULT FALSE,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_results_school_student
  ON public.results (school_id, student_id);

CREATE INDEX IF NOT EXISTS idx_results_school_class
  ON public.results (school_id, class_id);

CREATE INDEX IF NOT EXISTS idx_results_deleted
  ON public.results (is_deleted);

